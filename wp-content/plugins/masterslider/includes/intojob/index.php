 Слагается иногда картина чудовищная, но обстановка и весь процесс всего представления бывают при этом до того вероятны и с такими тонкими, неожиданными, но художественно соответствующими всей полноте картины подробностями, что их и не выдумать наяву этому же самому сновидцу, будь он такой же художник, как Пушкин или Тургенев. «Как это они так все шумят!» — мелькнуло в его голове. Одним словом, я вывожу, что и все, не то что великие, но и чуть-чуть из колеи выходящие люди, то есть чуть-чуть даже способные сказать что-нибудь новенькое, должны, по природе своей, быть непременно преступниками, — более или менее, разумеется. Та сбежала последнюю лестницу и остановилась вплоть перед ним, ступенькой выше его. Я тут также ввязался и распоряжался.
 Если Софья Семеновна не воротится через десять минут, то я пришлю ее к вам самое, если хотите, сегодня же; ну вот и мой нумер. Ему уже много раз случалось проходить, например, домой и совершенно не помнить дороги, по которой он шел, и он уже привык так ходить. Экономическая же правда прибавляет, что чем более в обществе устроенных частных дел и, так сказать, целых кафтанов, тем более для него твердых оснований и тем более устраивается в нем и общее дело. Он кой-что и вчера еще слышал об этих поминках; даже помнилось, как будто и его приглашали; но за собственными хлопотами он все это остальное пропустил без внимания.
 Поспешив осведомиться у госпожи Липпевехзель, хлопотавшей в отсутствие Катерины Ивановны (находившейся на кладбище) около накрывавшегося стола, он узнал, что поминки будут торжественные, что приглашены почти все жильцы, из них даже и незнакомые покойному, что приглашен даже Андрей Семенович Лебезятников, несмотря на бывшую его ссору с Катериной Ивановной, и, наконец, он сам, Петр Петрович, не только приглашен, но даже с большим нетерпением ожидается, так как он почти самый важный гость из всех жильцов.
 Сама Амалия Ивановна приглашена была тоже с большим почетом, несмотря на все бывшие неприятности, а потому хозяйничала и хлопотала теперь, почти чувствуя от этого наслаждение, а сверх того была вся разодета хоть и в траур, но во все новое, в шелковое, в пух и прах, и гордилась этим. В противном же случае принужден буду обратиться к мерам весьма серьезным, тогда… пеняйте уже на себя-с!
 Совершенное молчание воцарилось в комнате. Кабинет его была комната ни большая, ни маленькая; стояли в ней: большой письменный стол перед диваном, обитым клеенкой, бюро, шкаф в углу и несколько стульев — все казенной мебели, из желтого отполированного дерева.
 В углу, в задней стене или, лучше сказать, в перегородке была запертая дверь: там далее, за перегородкой, должны были, стало быть, находиться еще какие-то комнаты. Кроме того, в оправдание Дуни, явились, наконец, и свидетельства слуг, которые видели и знали гораздо больше, чем предполагал сам господин Свидригайлов, как это и всегда водится. А ведь вот именно теперь-то и не придете…»
 Ему вдруг почему-то вспомнилось, как давеча, за час до исполнения замысла над Дунечкой, он рекомендовал Раскольникову поручить ее охранению Разумихина. Это очень нехорошо-с. Болезнь Пульхерии Александровны была какая-то странная, нервная и сопровождалась чем-то вроде помешательства, если не совершенно, то, по крайней мере, отчасти. Он несколько успокоился, но ненадолго. Его превосходительство Ивана Афанасьевича изволите знать?..
 Нет? Ну так божия человека не знаете! Это — воск… воск перед лицом господним; яко тает воск!… Даже прослезились, изволив все выслушать. Близко.
 На канаве, не очень далеко от моста и не доходя двух домов от дома, где жила Соня, столпилась куча народу. Не то чтоб уж я его очень терпеть не мог, но через него, однако, и вышла эта ссора моя с Марфой Петровной, когда я узнал, что она эту свадьбу состряпала. Они шли служить панихиду. Ему сделалось и тяжело, и душно, и как-то неловко, что он пришел сюда. Прошлого года уверил нас для чего-то, что в монахи идет: два месяца стоял на своем! Недавно вздумал уверять, что женится, что все уж готово к венцу. Вернее всего было то, что один из них что-то украл и даже успел тут же продать какому-то подвернувшемуся жиду; но, продав, не захотел поделиться с своим товарищем.
 Оказалось, наконец, что проданный предмет была чайная ложка, принадлежавшая вокзалу. Теперь же состояние его походило на какой-то даже восторг, и в то же время как будто все выпитое вино вновь, разом и с удвоенною силой, бросилось ему в голову.
 Он стоял с обеими дамами, схватив их обеих за руки, уговаривая их и представляя им резоны с изумительною откровенностью, и, вероятно для большего убеждения, почти при каждом слове своем, крепко-накрепко, как в тисках, сжимал им обеим руки до боли и, казалось, пожирал глазами Авдотью Романовну, нисколько этим не стесняясь. Ну-с, до свидания… Добрых мыслей, благих начинаний!
 Порфирий вышел, как-то согнувшись и как бы избегая глядеть на Раскольникова. Нет, не убежите, Родион Романыч.
 Раскольников встал с места и взял фуражку. Она вскакивала иногда, плакала, руки ломала, то забывалась опять лихорадочным сном, и ей снились Полечка, Катерина Ивановна, Лизавета, чтение Евангелия и он… он, с его бледным лицом, с горящими глазами… Он целует ей ноги, плачет… О господи!
 За дверью справа, за тою самою дверью, которая отделяла квартиру Сони от квартиры Гертруды Карловны Ресслих, была комната промежуточная, давно уже пустая, принадлежавшая к квартире госпожи Ресслих и отдававшаяся от нее внаем, о чем и выставлены были ярлычки на воротах и наклеены бумажечки на стеклах окон, выходивших на канаву. А сам, будьте уверены, вам мешать не стану.
 Действительно, минут через пять Лебезятников возвратился с Сонечкой.
 Та вошла в чрезвычайном удивлении и, по обыкновению своему, робея. Он отвел от губ трубку и уже хотел спрятаться; но, поднявшись и отодвинув стул, вероятно, вдруг заметил, что Раскольников его видит и наблюдает. Торжество самосохранения, спасение от давившей опасности — вот что наполняло в эту минуту все его существо, без предвидения, без анализа, без будущих загадываний и отгадываний, без сомнений и без вопросов. Ну а чуть заболел, чуть нарушился нормальный земной порядок в организме, тотчас и начинает сказываться возможность другого мира, и чем больше болен, тем и соприкосновений с другим миром больше, так что когда умрет совсем человек, то прямо и перейдет в другой мир»</p>
