 — Вы нам все вчера отдали! — проговорила вдруг в ответ Сонечка, каким-то сильным и скорым шепотом, вдруг опять сильно потупившись. В то время он и сам еще не верил этим мечтам своим и только раздражал себя их безобразною, но соблазнительною дерзостью. Сначала сам добивался от Сонечки, а тут и в амбицию вдруг вошли: «Как, дескать, я, такой просвещенный человек, в одной квартире с таковскою буду жить?» А Катерина Ивановна не спустила, вступилась… ну и произошло… И заходит к нам Сонечка теперь более в сумерки, и Катерину Ивановну облегчает, и средства посильные доставляет.
 Живет же на квартире у портного Капернаумова, квартиру у них снимает, а Капернаумов хром и косноязычен, и все многочисленнейшее семейство его тоже косноязычное. Ну вот, например, ведь вы пошли ко мне теперь мало того что по делу, а за чем-нибудь новеньким? Ведь так? Ведь так? — настаивал Свидригайлов с плутовскою улыбкой, — ну, представьте же себе после этого, что я сам-то, еще ехав сюда, в вагоне, на вас же рассчитывал, что вы мне тоже скажете что-нибудь новенького и что от вас же удастся мне чем-нибудь позаимствоваться! Вот какие мы богачи!
 — Чем это позаимствоваться?
 — Да что вам сказать? Разве я знаю чем? Видите, в каком трактиришке все время просиживаю, и это мне всласть, то есть не то чтобы всласть, а так, надо же где-нибудь сесть. Это я наверное, даже по фактам, знаю-с.
 Он только сам не знает, что я знаю. Даже Раскольников, хоть и не разговаривал, но некоторое время внимательно слушал. Кажется, и все беднейшее семейство это от вас одной теперь только и зависит.
 — Позвольте спросить, — вдруг встала Соня, — вы ей что изволили говорить вчера о возможности пенсиона? Потому, она еще вчера говорила мне, что вы взялись ей пенсион выхлопотать. Он долго смотрел на них и, наконец, свободною правою рукой начал ловить одну муху. Люди убивали друг друга в какой-то бессмысленной злобе. Раскольников приподнялся.
 — Это кто, Настасья? — спросил он, указывая на парня.
 — Ишь ведь, очнулся! — сказала она — Очнулись, — отозвался артельщик. Они точно не понимают и понять не могут, но вовсе не конфузятся. Узнав в свою очередь, что он об ней так тоскует и заботится, Соня прислала ему записку, написанную карандашом, и уведомляла его, что ей гораздо легче, что у ней пустая, легкая простуда и что она скоро, очень скоро, придет повидаться с ним на работу. Вот сегодняшние-с. Соня стояла мертво-бледная, смотрела на Лужина и ничего не могла отвечать. Я тороплюсь, мне надо идти со двора…
 — С величайшим удовольствием. (Катерина Ивановна уже успела создать себе эту фантазию и поверить ей слепо.) Пускай, пускай этот негодный генералишка видит. Но, несмотря на то, что этот новый факт чрезвычайно его беспокоил, Раскольников как-то не спешил разъяснением дела. Какая-то особенная тоска начала сказываться ему в последнее время. Произошло у них тут же в саду ужасная сцена:
 Марфа Петровна даже ударила Дуню, не хотела ничего слушать, а сама целый час кричала и, наконец, приказала тотчас же отвезти Дуню ко мне в город, на простой крестьянской телеге, в которую сбросили все ее вещи, белье, платья, все как случилось, неувязанное и неуложенное</p>
