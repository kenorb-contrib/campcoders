 — Ну что ж… в трех шагах и нельзя не убить. Эту ночь и сестра твоя всю напролет в бреду пролежала и все о тебе вспоминала. Она чужую жизнь заедает: она намедни Лизавете палец со зла укусила; чуть-чуть не отрезали!
 — Конечно, она недостойна жить, — заметил офицер, — но ведь тут природа.
 — Эх, брат, да ведь природу поправляют и направляют, а без этого пришлось бы потонуть в предрассудках. И каким образом это случилось, что она рискнула взять такую раскрасавицу в свой дом, в гувернантки! Я объясняю тем, что Марфа Петровна была женщина пламенная и восприимчивая и что, просто-запросто, она сама влюбилась, — буквально влюбилась, — в вашу сестрицу. Даже в интонации его голоса было на этот раз нечто уж особенно ясное.
 — Уж не Наполеон ли какой будущий и нашу Алену Ивановну на прошлой неделе топором укокошил? — брякнул вдруг из угла Заметов.
 Раскольников молчал и пристально, твердо смотрел на Порфирия.
 Разумихин мрачно нахмурился. Волосы его, очень еще густые, были совсем белокурые и чуть-чуть разве с проседью, а широкая, густая борода, спускавшаяся лопатой, была еще светлее головных волос. В последнюю встречу Свидригайлов объяснил Раскольникову, что с детьми Катерины Ивановны он как-то покончил, и покончил удачно; что у него, благодаря кой-каким связям, отыскались такие лица, с помощью которых можно было поместить всех троих сирот, немедленно, в весьма приличные для них заведения; что отложенные для них деньги тоже многому помогли, так как сирот с капиталом поместить гораздо легче, чем сирот нищих. Заметов, он соскандалит что-нибудь на французский манер в неприличном заведении, за стаканом шампанского или донского, — вот что такое ваш Заметов! А я, может быть, так сказать, сгорел от преданности и высоких чувств и сверх того имею значение, чин, занимаю место! Женат и имею детей. Тот тотчас же заметил его, быстро оглядел, но опять опустил глаза, и так шли они с минуту, один подле другого и не говоря ни слова.
 — Вы меня спрашивали… у дворника? — проговорил наконец Раскольников, но как-то очень негромко.
 Мещанин не дал никакого ответа и даже не поглядел. Вдруг он вспомнил, что кошелек и вещи, которые он вытащил у старухи из сундука, все до сих пор у него по карманам лежат! Он и не подумал до сих пор их вынуть и спрятать! Не вспомнил о них даже теперь, как платье осматривал! Что же это? Мигом бросился он их вынимать и выбрасывать на стол. Вам без занятий оставаться нельзя, а потому труд и твердо поставленная перед собою цель, мне кажется, очень бы могли вам помочь.
 — Да, да, вы совершенно правы… вот я поскорей поступлю в университет, и тогда все пойдет… как по маслу…
 Зосимов, начавший свои умные советы отчасти и для эффекта перед дамами, был, конечно, несколько озадачен, когда, кончив речь и взглянув на своего слушателя, заметил в лице его решительную насмешку. Сонина комната походила как будто на сарай, имела вид весьма не правильного четырехугольника, и это придавало ей что-то уродливое. Я, знаете, труслив-с, поехал намедни к Б-ну, — каждого больного minimum по получасу осматривает; так даже рассмеялся, на меня глядя: и стукал, и слушал, — вам, говорит, между прочим, табак не годится; легкие расширены. А между тем ведь это так-с, с иным субъектом особенно, потому люди многоразличны-с, и над всем одна практика-с. В первое мгновение она ужасно испугалась, и все лицо ее помертвело. Без нас вам нельзя обойтись. В бреду вырывались у ней слова, по которым можно было заключить, что она гораздо более подозревала в ужасной судьбе сына, чем даже предполагали.
 Раскольников долго не знал о смерти матери, хотя корреспонденция с Петербургом установилась еще с самого начала водворения его в Сибири.
 Устроилась она через Соню, которая аккуратно каждый месяц писала в Петербург на имя Разумихина и аккуратно каждый месяц получала из Петербурга ответ</p>
